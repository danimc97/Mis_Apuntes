package marvel_VS_SF;

public class DetectaPunetazoEvent {

	String punetazo;

	
	
	public DetectaPunetazoEvent() {
		super();
	}

	public DetectaPunetazoEvent(String punetazo) {
		super();
		this.punetazo = punetazo;
	}

	public String getPunetazo() {
		return punetazo;
	}

	public void setPunetazo(String punetazo) {
		this.punetazo = punetazo;
	}
	
		
}
